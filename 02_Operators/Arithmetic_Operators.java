public class Arithmetic_Operators {
    public static void main(String[] args) {
        
    //Arithmetic operators are used to perform common mathematical operations

    int num_1 = 10;
    int num_2 = 5;

    System.out.println("Addition operator(+) adds two values : "+num_1+" + "+num_2+" = "+(num_1 + num_2));
    System.out.println("Subtraction operator(-) subtracts one value from another : "+num_1+" - "+num_2+" = "+(num_1 - num_2));
    System.out.println("Multiplication operator(*) multiplies two values : "+num_1+" * "+num_2+" = "+(num_1 * num_2));
    System.out.println("Division operator(/) divides one value by another : "+num_1+" / "+num_2+" = "+(num_1 / num_2));
    System.out.println("Modulus operator(%) returns the remainder : "+num_1+" % "+num_2+" = "+(num_1 % num_2));
    System.out.println("Increment operator(++) increases the value by 1 : "+"++"+num_1+" = "+(++num_1));
    System.out.println("Decrement operator(--) decreases the value by 1 : "+"--"+num_2+" = "+(--num_2));
    }
}
