public class Comparison_Operators{
    public static void main(String[] args) {
        int a = 6;
        int b = 8;

       /*Comparison operators are used to compare two values (or variables).
        The return value of a comparison is either true or false.*/

        System.out.println("(==)Equal to for : "+a+" == "+b+" is "+(a==b));
        System.out.println("(!=)Not Equal to for : "+a+" != "+b+" is "+(a!=b));
        System.out.println("(>)Greater than for : "+a+" > "+b+" is "+(a>b));
        System.out.println("(<)Less than for : "+a+" < "+b+" is "+(a<b));
        System.out.println("(>=)Greater than or Equal to for : "+a+" >= "+b+" is "+(a>=b));
        System.out.println("(<=)Less than or Equal to for : "+a+" <= "+b+" is "+(a<=b));
    }
}